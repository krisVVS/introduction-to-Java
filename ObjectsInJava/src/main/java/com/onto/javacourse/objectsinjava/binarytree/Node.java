package com.onto.javacourse.objectsinjava.binarytree;

/** Represents a node in a binary tree. */
public class Node {
  int value;
  Node left;
  Node right;

  /**
   * Creates a new node with a specified value.
   *
   * @param value The value to be stored in the node.
   */
  public Node(int value) {
    this.value = value;
  }
}
